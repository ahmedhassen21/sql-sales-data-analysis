/* =====================================================
CUSTOMER EXPLORATION
===================================================== */

--- Explore All Countries Our Customers Come From
SELECT DISTINCT country
FROM Gold.dim_customers;

--- Explore the Oldest and Youngest Customer
SELECT 
    MAX(birthdate) AS Youngest_customer,
    DATEDIFF(YEAR, MAX(birthdate), GETDATE()) AS Year_of_youngest,
    MIN(birthdate) AS Oldest_Customer,
    DATEDIFF(YEAR, MIN(birthdate), GETDATE()) AS Year_of_oldest
FROM Gold.dim_customers;

--- Find Total Customers
SELECT COUNT(customer_key) AS Total_Customers
FROM Gold.dim_customers;

--- Find Total Customers Who Placed Orders
SELECT COUNT(DISTINCT customer_key) AS Customers_With_Orders
FROM Gold.fact_sales;

--- Find Total Customers by Country
SELECT 
    country,
    COUNT(customer_key) AS total_customer
FROM Gold.dim_customers
GROUP BY country
ORDER BY total_customer DESC;

--- Find Total Customers by Gender
SELECT 
    gender,
    COUNT(customer_key) AS total_customer
FROM Gold.dim_customers
GROUP BY gender
ORDER BY total_customer DESC;


/* =====================================================
PRODUCT EXPLORATION
===================================================== */

--- Explore Product Hierarchy
SELECT DISTINCT
    category,
    subcategory,
    product_name
FROM Gold.dim_products
ORDER BY 1,2,3;

--- Find Total Number of Products
SELECT COUNT(DISTINCT product_key) AS Total_Products
FROM Gold.dim_products;

--- Find Total Products by Category
SELECT
    category,
    COUNT(product_key) AS total_product
FROM Gold.dim_products
GROUP BY category
ORDER BY total_product DESC;

--- Average Product Cost by Category
SELECT
    category,
    AVG(product_cost) AS average_cost
FROM Gold.dim_products
GROUP BY category
ORDER BY average_cost DESC;


/* =====================================================
SALES EXPLORATION
===================================================== */

--- Find the First and Last Order Date
--- Calculate How Many Years of Sales Are Available
SELECT 
    MIN(order_date) AS First_order_date,
    MAX(order_date) AS Last_order_date,
    DATEDIFF(YEAR, MIN(order_date), MAX(order_date)) AS years_of_sales
FROM Gold.fact_sales;

--- Find Total Sales
SELECT 
    SUM(sales_amount) AS Total_Sales
FROM Gold.fact_sales;

--- Find Total Quantity Sold
SELECT 
    SUM(quantity) AS Total_items
FROM Gold.fact_sales;

--- Find Average Selling Price
SELECT 
    AVG(price) AS Average_price
FROM Gold.fact_sales;

--- Find Total Number of Orders
SELECT 
    COUNT(DISTINCT order_number) AS Total_Orders
FROM Gold.fact_sales;


