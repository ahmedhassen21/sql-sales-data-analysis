/* =====================================================
REVENUE ANALYSIS
===================================================== */

--- Total Revenue Generated by Category
SELECT
    dp.category,
    SUM(fs.sales_amount) AS Total_revenue
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_products dp
    ON dp.product_key = fs.product_key
GROUP BY dp.category
ORDER BY Total_revenue DESC;

--- Total Revenue Generated by Customer
SELECT
    dc.customer_key,
    dc.first_name,
    dc.last_name,
    SUM(fs.sales_amount) AS Total_revenue
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_customers dc
    ON dc.customer_key = fs.customer_key
GROUP BY dc.customer_key, dc.first_name, dc.last_name
ORDER BY Total_revenue DESC;

--- Distribution of Sold Items Across Countries
SELECT
    dc.country,
    SUM(fs.quantity) AS Total_sold_items
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_customers dc
    ON dc.customer_key = fs.customer_key
GROUP BY dc.country
ORDER BY Total_sold_items DESC;


/* =====================================================
TOP PRODUCTS ANALYSIS
===================================================== */

--- Top 5 Products by Revenue
SELECT TOP(5)
    dp.product_name,
    SUM(fs.sales_amount) AS Total_revenue
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_products dp
    ON dp.product_key = fs.product_key
GROUP BY dp.product_name
ORDER BY Total_revenue DESC;

--- Top 5 Products Using Ranking
SELECT *
FROM (
    SELECT
        dp.product_name,
        SUM(fs.sales_amount) AS Total_revenue,
        RANK() OVER(ORDER BY SUM(fs.sales_amount) DESC) AS rank_products
    FROM Gold.fact_sales fs
    LEFT JOIN Gold.dim_products dp
        ON dp.product_key = fs.product_key
    GROUP BY dp.product_name
) t
WHERE rank_products <= 5;

--- 5 Worst Performing Products
SELECT TOP(5)
    dp.product_name,
    SUM(fs.sales_amount) AS Total_revenue
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_products dp
    ON dp.product_key = fs.product_key
GROUP BY dp.product_name
ORDER BY Total_revenue;


/* =====================================================
CUSTOMER PERFORMANCE
===================================================== */

--- Top 10 Customers by Revenue
SELECT *
FROM (
    SELECT
        dc.customer_key,
        dc.first_name,
        dc.last_name,
        SUM(fs.sales_amount) AS Total_revenue,
        ROW_NUMBER() OVER(ORDER BY SUM(fs.sales_amount) DESC) AS rank_customer
    FROM Gold.fact_sales fs
    LEFT JOIN Gold.dim_customers dc
        ON dc.customer_key = fs.customer_key
    GROUP BY dc.customer_key, dc.first_name, dc.last_name
) t
WHERE rank_customer <= 10;

--- 3 Customers With the Fewest Orders
SELECT TOP 3
    dc.customer_key,
    dc.first_name,
    dc.last_name,
    COUNT(DISTINCT fs.order_number) AS Total_orders
FROM Gold.fact_sales fs
LEFT JOIN Gold.dim_customers dc
    ON dc.customer_key = fs.customer_key
GROUP BY dc.customer_key, dc.first_name, dc.last_name
ORDER BY Total_orders;
